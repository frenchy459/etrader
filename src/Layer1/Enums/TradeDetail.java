package Layer1.Enums;

/** Contains members representing various transaction properties */
public enum TradeDetail {
    /** represents the transaction type */
    TYPE,

    /** represents the transaction status */
    STATUS,

    /** represents the initial meeting location of the transaction */
    INITIAL_LOCATION,

    /** represents the returning meeting location of the transaction */
    RETURN_LOCATION,

    /** represents the initial meeting date of the transaction */
    INITIAL_DATE,

    /** represents the returning meeting date of the transaction */
    RETURN_DATE
}
